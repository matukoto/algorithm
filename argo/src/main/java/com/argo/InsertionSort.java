package com.argo;

import java.util.Scanner;

public class InsertionSort {
  public static void main(String[] args) throws Exception {
    // 標準入力を取得
    Scanner sc = new Scanner(System.in);
    int N = sc.nextInt();
    int i = 0;
    for (; i < N; i++) {
      i++;
    }
    System.out.println(i);
    sc.close();
  }
}
